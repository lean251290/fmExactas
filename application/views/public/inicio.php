<h3><strong>¿QUIÉN CONDUCE EL CENTRO DE ESTUDIANTES?</strong></h3>
Actualmente el Centro de Estudiantes esta conducido por la Agrupación Franja Morada, que tiene como objetivo la Defensa de la Educación Pública de Acceso Libre, Gratuito y de Excelencia Académica. Es por ello que luchamos contra todo tipo de examen de ingreso eliminatorio y de arancel, ya que la educación es un derecho de todos sin importar estrato social, credo o tendencia política.
Esta agrupación es una de las organizaciones estudiantiles más numerosas e importante del país y de Latinoamérica con sede en cada uno de los Centros de Estudiantes de las Universidades del país, que cuenta además con una estructura de conducción nacional, la cual se encarga de coordinar las actividades de los distintos centros de estudiantes a nivel regional para lograr que el trabajo sea mucho más efectivo.
Esta agrupación además de tener una estructura nacional y de ser una de las más importantes de país, <strong>DEFIENDE LA GRATUIDAD DE LA ENSEÑANZA, EL INGRESO LIBRE E IRRESTRICTO Y ESTÁ EN CONTRA DE TODO TIPO DE ARANCELAMIENTO,</strong>
esta en contra de los exámenes de ingreso de carácter eliminatorio, que coartan el derecho a aprender y educarse libremente como necesidad natural de las personas, siendo que los exámenes eliminatorios no son una solución a los bajos índices de aprendizajes con los que los jóvenes ingresan a la Universidad, es por ello que la Franja Morada propone alternativas más viables, lógicas y coherentes por la realidad que se vive hoy, que es presa de nuestro país, para que no sea tan frustrante tus primeros pasos dentro de la vida universitaria.
La Franja Morada además pelea en contra del recorte presupuestario que afecta a la universidad y que te afecta a vos quitándote la oportunidad de acceder a una educación de excelencia con las condiciones de trabajos adecuadas para el buen desenvolvimiento del estudiante.
Es por ello que la Franja Morada no baja los brazos en su lucha, sigue y seguirá defendiendo la bandera de la educación pública, gratuita y de calidad.
